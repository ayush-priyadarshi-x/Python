 # Integer
age = 25  # Represents an integer value

# Float
price = 19.99  # Represents a floating-point number

# String
name = "Ayush"  # Represents a sequence of characters (text)

# Boolean
is_student = True  # Represents a boolean value (True or False)

# List (ordered, mutable collection)
fruits = ["apple", "banana", "cherry"]  # Represents a list of items (similar to arrays)

# Tuple (ordered, immutable collection)
coordinates = (10, 20)  # Represents an immutable sequence of items

# Set (unordered collection of unique items)
unique_numbers = {1, 2, 3, 4}  # Represents a set with unique items only

# Dictionary (key-value pairs, unordered)
person = {
    "name": "Ayush",
    "age": 25,
    "is_student": True
}  # Represents key-value pairs, like a JavaScript object

# None (represents the absence of a value)
unknown_value = None  # Represents a null or undefined value

# Complex number (used in mathematical calculations)
complex_number = 3 + 4j  # Represents a complex number with real and imaginary parts
